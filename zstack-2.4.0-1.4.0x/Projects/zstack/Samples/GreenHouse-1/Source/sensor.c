/*********************************************************************************************
* 名称：sensorControl()
* 功能：传感器控制
* 参数：cmd - 控制命令
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void sensorControl(uint8 cmd)
{
    static uint8 stepmotor_dir=0;
    
    /* 电机控制 */
    if(cmd & 0x01)
    {
        if(!stepmotor_dir)
        {
            stepmotor_dir=1;
            forward(5000);
        }
    }
    else
    {
        if(stepmotor_dir)
        {
            stepmotor_dir=0;
            reversion(5000);
        }
    }

    /* 风扇控制 */
    if(cmd & 0x02)
    {
        FANIO = FAN_ON;                                         // 开启风扇
    }
    else
    {
        FANIO = FAN_OFF;                                        // 开启风扇
    }
    
    /*继电器控制*/
    if(D1&0x04)
        relay_on(0x01);
    else
        relay_off(0x01);
    
    if(D1&0x08)
        relay_on(0x02);
    else
        relay_off(0x02);
}
/*********************************************************************************************
* 名称：ZXBeeUserProcess()
* 功能：解析收到的控制命令
* 参数：*ptag -- 控制命令名称
*       *pval -- 控制命令参数
* 返回：<0:不支持指令，>=0 指令已处理
* 修改：
* 注释：
*********************************************************************************************/
int ZXBeeUserProcess(char *ptag, char *pval)
{
    int val;
    int ret = 0;
    char pData[16];
    char *p = pData;

    // 将字符串变量pval解析转换为整型变量赋值
    val = atoi(pval);
    // 控制命令解析
    if (0 == strcmp("CD0", ptag))                                 // 对D0的位进行操作，CD0表示位清零操作
    {
        D0 &= ~val;
    }
    if (0 == strcmp("OD0", ptag))                                 // 对D0的位进行操作，OD0表示位置一操作
    {
        D0 |= val;
    }
    if (0 == strcmp("D0", ptag))                                  // 查询上报使能编码
    {
        if (0 == strcmp("?", pval))
        {
            ret = sprintf(p, "%u", D0);
            ZXBeeAdd("D0", p);
        }
    }
    if (0 == strcmp("CD1", ptag))                                 // 对D1的位进行操作，CD1表示位清零操作
    {
        D1 &= ~val;
        sensorControl(D1);                                          // 处理执行命令
    }
    if (0 == strcmp("OD1", ptag))                                 // 对D1的位进行操作，OD1表示位置一操作
    {
        D1 |= val;
        sensorControl(D1);                                          // 处理执行命令
    }
    if (0 == strcmp("D1", ptag))                                  // 查询执行器命令编码
    {
        if (0 == strcmp("?", pval))
        {
            ret = sprintf(p, "%u", D1);
            ZXBeeAdd("D1", p);
        }
    }
    if (0 == strcmp("A0", ptag))
    {
        if (0 == strcmp("?", pval))
        {
            updateA0();
            ret = sprintf(p, "%.1f", A0);
            ZXBeeAdd("A0", p);
        }
    }
    if (0 == strcmp("A1", ptag))
    {
        if (0 == strcmp("?", pval))
        {
            updateA1();
            ret = sprintf(p, "%.1f", A1);
            ZXBeeAdd("A1", p);
        }
    }
    if (0 == strcmp("A2", ptag))
    {
        if (0 == strcmp("?", pval))
        {
            updateA2();
            ret = sprintf(p, "%.1f", A2);
            ZXBeeAdd("A2", p);
        }
    }
    if (0 == strcmp("A3", ptag))
    {
        if (0 == strcmp("?", pval))
        {
            updateA3();
            ret = sprintf(p, "%u", A3);
            ZXBeeAdd("A3", p);
        }
    }
    if (0 == strcmp("A7", ptag))
    {
        if (0 == strcmp("?", pval))
        {
            //updateA7();
            ret = sprintf(p, "%.1f", A7);
            ZXBeeAdd("A7", p);
        }
    }
    if (0 == strcmp("V0", ptag))
    {
        if (0 == strcmp("?", pval))
        {
            ret = sprintf(p, "%u", V0);                         	// 上报时间间隔
            ZXBeeAdd("V0", p);
        }
        else
        {
            updateV0(pval);
        }
    }
    
    return ret;
}
/*********************************************************************************************
* 名称：MyEventProcess()
* 功能：自定义事件处理
* 参数：event -- 事件编号
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void MyEventProcess( uint16 event )
{
    if (event & MY_REPORT_EVT)
    {
        sensorUpdate();
        //启动定时器，触发事件：MY_REPORT_EVT
        osal_start_timerEx(sapi_TaskID, MY_REPORT_EVT, V0*1000);
    }
    if (event & MY_CHECK_EVT)
    {
        // 启动定时器，触发事件：MY_CHECK_EVT
        osal_start_timerEx(sapi_TaskID, MY_CHECK_EVT, 100);
        sensorCheck();
    }
}