/*********************************************************************************************
* 名称：sensorUpdate()
* 功能：处理主动上报的数据
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void sensorUpdate(void)
{
    char pData[16];
    char *p = pData;

    
    ZXBeeBegin();                                                 // 智云数据帧格式包头

    // 根据D0的位状态判定需要主动上报的数值
    if ((D0 & 0x01) == 0x01)                                      // 若温度上报允许，则pData的数据包中添加温度数据
    {
        updateA0();
        sprintf(p, "%.1f", A0);
        ZXBeeAdd("A0", p);
    }
    if ((D0 & 0x02) == 0x02)                                      // 若湿度上报允许，则pData的数据包中添加湿度数据
    {
        updateA1();
        sprintf(p, "%.1f", A1);
        ZXBeeAdd("A1", p);
    }
    if ((D0 & 0x04) == 0x04)                                      // 若光照上报允许，则pData的数据包中添加光照数据
    {
        updateA2();
        sprintf(p, "%.1f", A2);
        ZXBeeAdd("A2", p);
    }
    if ((D0 & 0x08) == 0x08)                                      // 若空气质量上报允许，则pData的数据包中添加空气质量数据
    {
        updateA3();
        sprintf(p, "%u", A3);
        ZXBeeAdd("A3", p);
    }
    if ((D0 & (1<<7)) == (1<<7))                                      // 若大气压力上报允许，则pData的数据包中添加大气压力数据
    {
        //updateA7();
        sprintf(p, "%.1f", A7);
        ZXBeeAdd("A7", p);
    }

    sprintf(p, "%u", D1);                                  // 上报控制编码
    ZXBeeAdd("D1", p);

    p = ZXBeeEnd();                                               // 智云数据帧格式包尾
    if (p != NULL)
    {
        ZXBeeInfSend(p, strlen(p));	                                // 将需要上传的数据进行打包操作，并通过zb_SendDataRequest()发送到协调器
    }
}

