/*********************************************************************************************
* 名称：BatteryVoltage_init()
* 功能：电池电压测量初始化
* 参数：
* 返回：
* 修改：
* 注释：
*********************************************************************************************/
void BatteryVoltage_init()
{
    P0SEL |= (1<<4);                                            //端口0_4 功能选择外设功能
    P0DIR &= ~(1<<4);                                           //设置输入模式
    APCFG |= (1<<4);                                            //模拟 I/O 使能
}

/*********************************************************************************************
* 名称：BatteryVoltage_init()
* 功能：电池电压测量初始化
* 参数：
* 返回：
* 修改：
* 注释：
*********************************************************************************************/
float BatteryVoltage_Get()
{
    unsigned short adcValue=0;
    float value=0;

    adcValue = HalAdcRead(HAL_ADC_CHANNEL_4,HAL_ADC_RESOLUTION_12);
    value = ((float)adcValue*(3.3/2047.0))*13.6/3.6;
    return value;                                               //返回有效值
}

