#include "relay.h"

/*********************************************************************************************
* 名称：relay_init()
* 功能：继电器传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void relay_init(void)
{
  P1SEL &= ~((1<<2)+(1<<3));                                               //配置管脚为通用IO模式
  P1DIR |= (1<<2)+(1<<3);                                                //配置控制管脚为输出模式
  relay_off(0x03);
}

void relay_on(uint8 status)
{
    if(status&0x01)
        RELAY1 = ON;                                            // 开启继电器1
    
    if(status&0x02)
        RELAY2 = ON;                                            // 开启继电器1
}

void relay_off(uint8 status)
{
    if(status&0x01)
        RELAY1 = OFF;                                           // 关闭继电器1
    
    if(status&0x02)
        RELAY2 = OFF;                                           // 关闭继电器1
}
