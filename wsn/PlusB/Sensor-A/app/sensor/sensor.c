/*********************************************************************************************
* 名称：sensor_control()
* 功能：传感器控制
* 参数：cmd - 控制命令
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void sensor_control(unsigned char cmd)
{
    if((cmd & 0x40) == 0x40)
    {
        relay_on(1);
    }
    else
    {
        relay_off(1);
    }
    if((cmd & 0x80) == 0x80)
    {
        relay_on(2);
    }
    else
    {
        relay_off(2);
    }
}

