/*********************************************************************************************
* 名称：sensor_poll()
* 功能：轮询传感器，并主动上报传感器数据
* 参数：t: 调用次数
* 返回：
* 修改：
* 注释：此函数每秒钟调用1次，t为调用次数
*********************************************************************************************/
void sensor_poll(unsigned int t)
{
    char buf[16] = {0};
    static unsigned char stepTime = 0;
    updateA8();
    updateA9();
    updateA10();
    
    if(stepMotorEnable)
    {
        stepTime++;
        if(stepTime >= 2)
        {
            stepmotor_control(0);
            stepMotorEnable = 0;
            stepTime = 0;
        }
    }
    
    if (V0 != 0)
    {
        if (t % V0 == 0)
        {
            zxbeeBegin();
            if (D0 & 0x01)
            {
                sprintf(buf, "%d", A0);
                zxbeeAdd("A0", buf);
            }
            if (D0 & 0x02)
            {
               //A1值打包
            }
            if (D0 & 0x04)
            {
               //A2值打包
            }
            if (D0 & 0x08)
            {
               //A3值打包
            }
            if (D0 & 0x10)
            {
               //A4值打包
            }
            if (D0 & 0x20)
            {
                
            }
            if (D0 & 0x40)
            {
                
            }
            if (D0 & 0x80)
            {
                
            }
            sprintf(buf, "%.1f", A8);
            zxbeeAdd("A8", buf);
            sprintf(buf, "%.1f", A9);
            zxbeeAdd("A9", buf);
            sprintf(buf, "%.1f", A10);
            zxbeeAdd("A10", buf);
            char *p = zxbeeEnd();
            if (p != NULL)
            {
                rfUartSendData(p);                              //发送无线数据
            }
        }
    }
}

