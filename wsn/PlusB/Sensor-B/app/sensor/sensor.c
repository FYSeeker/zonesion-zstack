/*********************************************************************************************
* 名称：sensor_control()
* 功能：传感器控制
* 参数：cmd - 控制命令
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void sensor_control(unsigned char cmd)
{
    static unsigned char stepMotorFlag = 0; 
    if(CurrMode == 1)
    {
        if((cmd & 0x04) == 0x04)                                // 步进电机控制
        {
            if(stepMotorFlag != 1)
            {
                stepMotorFlag = 1;
                stepmotor_control(0x03);
                stepMotorEnable = 1;
            }
        }
        else
        {
            if(stepMotorFlag == 1)
            {
                stepMotorFlag = 0;
                stepmotor_control(0x01);
                stepMotorEnable = 1;
            }
        }
        if((cmd & 0x08) == 0x08)                                // 风扇控制
            fan_control(6000);
        else
            fan_control(0);
    }
    if(CurrMode == 2)
    {
        if((cmd & 0x01) == 0x01)                                // RGB灯控制
        {
            if((cmd & 0x02) == 0x02)
            {
                rgb_on(0x04);
                rgb_off(0x03);
            }
            else
            {
                rgb_on(0x01);
                rgb_off(0x06);
            }
        }
        else if((cmd & 0x02) == 0x02)
        {
            rgb_on(0x02);
            rgb_off(0x05);
        }
        else
            rgb_off(0x07);
        if((cmd & 0x08) == 0x08)                                // 蜂鸣器控制
            beep_on();
        else
            beep_off();
    }
    if((cmd & 0x10) == 0x10)                                    // 传感器LED1灯控制
        ext_led_on(0x01);
    else
        ext_led_off(0x01);
    if((cmd & 0x20) == 0x20)                                    // 传感器LED2灯控制
        ext_led_on(0x02);
    else
        ext_led_off(0x02);
    if((cmd & 0x40) == 0x40)                                    // 继电器1控制
        relay_on(1);
    else
        relay_off(1);
    if((cmd & 0x80) == 0x80)                                    // 继电器2控制
        relay_on(2);
    else
        relay_off(2);
}

