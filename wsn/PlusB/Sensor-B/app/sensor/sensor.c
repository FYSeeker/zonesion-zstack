/*********************************************************************************************
* 名称：sensor_init()
* 功能：
* 参数：
* 返回：
* 修改：
* 注释：
*********************************************************************************************/
void sensor_init(void)
{
    relay_init();                                               // 初始化控制类传感器（继电器）
    ext_led_init();                                             // 传感器LED初始化函数
    if(GPIO_ReadInputDataBit(GPIOB, GPIO_Pin_10) == 0)          // 判断当前跳线模式
    {
        CurrMode = 1;
        stepmotor_init();                                       // 初始化步进电机
        fan_init();                                             // 初始化风扇
    }
    else
    {
        CurrMode = 2;
        rgb_init();                                             // 初始化RGB灯
        beep_init();                                            // 初始化蜂鸣器
    }
}

