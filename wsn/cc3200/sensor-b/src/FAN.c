#include "FAN.h"

/*********************************************************************************************
* 名称：FAN_init()
* 功能：风扇传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void FAN_init(void)
{
  PRCMPeripheralClkEnable(PRCM_GPIOA1, PRCM_RUN_MODE_CLK);      //使能时钟
  PinTypeGPIO(PIN_01,PIN_MODE_0,0);                           //选择引脚为GPIO模式（gpio03）
  GPIODirModeSet(GPIOA1_BASE, G10_UCPINS, GPIO_DIR_MODE_OUT);   //设置GPIO14为输出模式
  
  FAN_off(0x01);
}
  
/*********************************************************************************************
* 名称：FAN_on()
* 功能：风扇控制打开函数
* 参数：
* 返回：0，打开LED成功，-1，参数错误
* 修改：
* 注释：
*********************************************************************************************/
signed char FAN_on(unsigned char fan)
{
  if(fan & 0x01){                                                //如果要打开风扇
   GPIOPinWrite(GPIOA1_BASE, G10_UCPINS, 0xff);
    return 0;
  }
  
  return -1;                                                    //参数错误，返回-1
}

/*********************************************************************************************
* 名称：FAN_off()
* 功能：
* 参数：
* 返回：0，关闭成功，-1，参数错误
* 修改：
* 注释：
*********************************************************************************************/
signed char FAN_off(unsigned char fan)
{
  if(fan &0x01){                                                //如果要关闭风扇
    GPIOPinWrite(GPIOA1_BASE, G10_UCPINS, 0x00);
    return 0;
  }

  return -1;                                                    //参数错误，返回-1
}