#include "buzzer.h"

/*********************************************************************************************
* 名称：buzzer_init()
* 功能：蜂鸣器传感器初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void buzzer_init(void)
{
  P0SEL &= ~0x10;                                               //配置管脚为通用IO模式
  P0DIR |= 0x10;                                                //配置控制管脚为输出模式
}

void buzzer_on()
{
  P0_4 = 0;
}
void buzzer_off()
{
  P0_4 = 1;
}